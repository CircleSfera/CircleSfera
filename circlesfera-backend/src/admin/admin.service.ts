  /** Enhanced stats with growth percentages and engagement metrics. */
  async getEnhancedStats() {
    const now = new Date();
    const oneWeekAgo = new Date(now.getTime() - 7 * 24 * 60 * 60 * 1000);
    const twoWeeksAgo = new Date(now.getTime() - 14 * 24 * 60 * 60 * 1000);
    const oneDayAgo = new Date(now.getTime() - 24 * 60 * 60 * 1000);

    const [
      totalUsers,
      totalPosts,
      totalStories,
      pendingReports,
      totalReports,
      newUsersThisWeek,
      newUsersLastWeek,
      newPostsThisWeek,
      newPostsLastWeek,
      totalLikes,
      totalComments,
      activeUsersToday,
    ] = await Promise.all([
      this.prisma.user.count(),
      this.prisma.post.count(),
      this.prisma.story.count(),
      this.prisma.report.count({ where: { status: ReportStatus.PENDING } }),
      this.prisma.report.count(),
      this.prisma.user.count({
        where: { createdAt: { gte: oneWeekAgo } },
      }),
      this.prisma.user.count({
        where: { createdAt: { gte: twoWeeksAgo, lt: oneWeekAgo } },
      }),
      this.prisma.post.count({
        where: { createdAt: { gte: oneWeekAgo } },
      }),
      this.prisma.post.count({
        where: { createdAt: { gte: twoWeeksAgo, lt: oneWeekAgo } },
      }),
      this.prisma.like.count(),
      this.prisma.comment.count(),
      this.prisma.user.count({
        where: {
          OR: [
            { isOnline: true },
            {
              lastSeenAt: {
                gte: oneDayAgo,
              },
            },
          ],
        },
      }),
    ]);

    // Fetch recent audit logs
    const recentAuditLogs = await this.prisma.adminAuditLog.findMany({
      take: 10,
      orderBy: { createdAt: 'desc' },
      include: {
        admin: {
          include: { profile: { select: { username: true } } },
        },
      },
    });

    const calcGrowth = (current: number, previous: number) => {
      if (previous === 0) return current > 0 ? 100 : 0;
      return Math.round(((current - previous) / previous) * 100);
    };

    return {
      users: totalUsers,
      posts: totalPosts,
      stories: totalStories,
      pendingReports,
      newUsersThisWeek,
      userGrowth: calcGrowth(newUsersThisWeek, newUsersLastWeek),
      newPostsThisWeek,
      postGrowth: calcGrowth(newPostsThisWeek, newPostsLastWeek),
      engagement:
        totalPosts > 0
          ? Math.round(((totalLikes + totalComments) / totalPosts) * 100) / 100
          : 0,
      reportedContentPercent:
        totalPosts > 0
          ? Math.round((totalReports / totalPosts) * 10000) / 100
          : 0,
      activeUsersToday,
      recentActivity: (recentAuditLogs ?? []).map((log) => ({
        id: log.id,
        action: log.action,
        targetType: log.targetType,
        targetId: log.targetId,
        details: log.details,
        createdAt: log.createdAt,
        adminUsername: log.admin?.profile?.username || 'Unknown',
      })),
    };
  }

  /**
   * Sends a premium broadcast email to all active users.
   * Processes users in batches of 25 to ensure system stability and avoid Brevo rate limits.
   */
  async sendBroadcastEmail(adminId: string, dto: BroadcastEmailDto) {
    // 1. Get all active users
    const activeUsers = await this.prisma.user.findMany({
      where: { isActive: true },
      select: { email: true, id: true },
    });

    if (activeUsers.length === 0) {
      return { success: true, recipients: 0, sent: 0, failed: 0 };
    }

    const batchSize = 25;
    let successfulSent = 0;
    let failedSent = 0;

    // 2. Process in batches to avoid blocking and respect Brevo constraints
    for (let i = 0; i < activeUsers.length; i += batchSize) {
      const batch = activeUsers.slice(i, i + batchSize);

      const results = await Promise.allSettled(
        batch.map((user) =>
          this.emailService.sendBroadcastEmail(
            user.email,
            dto.subject,
            dto.title,
            dto.content,
            dto.buttonText,
            dto.buttonUrl,
          ),
        ),
      );

      for (const result of results) {
        if (result.status === 'fulfilled') {
          successfulSent++;
        } else {
          failedSent++;
        }
      }
    }

    // 3. Log the audit action
    await this.logAction(
      adminId,
      AdminAction.UPDATE_WHITELIST,
      'NEWSLETTER',
      'GLOBAL',
      `Subject: ${dto.subject} | Success: ${successfulSent} | Failed: ${failedSent}`,
    );

    return {
      success: true,
      recipients: activeUsers.length,
      sent: successfulSent,
      failed: failedSent,
    };
  }

  // ─── Users ────────────────────────────────────────────────────────

  /** Paginated users with optional search and status filter. Includes role and post count. */
  async getUsers(page = 1, limit = 10, search?: string, status?: string) {
    const skip = (page - 1) * limit;
    const where: Prisma.UserWhereInput = {};

    if (search) {
      where.OR = [
        { email: { contains: search, mode: 'insensitive' } },
        {
          profile: {
            username: { contains: search, mode: 'insensitive' },
          },
        },
        {
          profile: {
            fullName: { contains: search, mode: 'insensitive' },
          },
        },
      ];
    }

    if (status === 'active') where.isActive = true;
    if (status === 'banned') where.isActive = false;

    const [users, total] = await Promise.all([
      this.prisma.user.findMany({
        where,
        skip,
        take: limit,
        orderBy: { createdAt: 'desc' },
        include: {
          profile: true,
          _count: { select: { posts: true } },
        },
      }),
      this.prisma.user.count({ where }),
    ]);

    return {
      data: (users as unknown as UserWithVerification[]).map((u) => ({
        id: u.id,
        email: u.email,
        isActive: u.isActive,
        role: u.role,
        verificationLevel: u.verificationLevel,
        accountType: u.accountType,
        createdAt: u.createdAt,
        profile: u.profile,
        postCount: u._count.posts,
      })),
      meta: {
        total,
        page,
        limit,
        totalPages: Math.ceil(total / limit),
      },
    };
  }

  /** Ban a user, with audit logging. */
  async banUser(adminId: string, userId: string) {
    const result = await this.prisma.user.update({
      where: { id: userId },
      data: { isActive: false },
      include: { profile: true },
    });
    await this.logAction(adminId, AdminAction.BAN_USER, 'user', userId);
    await this.invalidateProfileCache(userId);

    // Notify user via email
    if (result.email) {
      await this.emailService.sendModerationEmail(
        result.email,
        result.profile?.fullName || result.profile?.username || 'Usuario',
        'suspendida',
        'Cuenta',
        'Violación de los Términos de Servicio',
      );
    }

    return result;
  }

