  /**
   * Update the moderation status of a piece of content.
   */
  async updateModerationStatus(
    adminId: string,
    targetType: 'POST' | 'STORY' | 'COMMENT',
    targetId: string,
    status: 'VISIBLE' | 'HIDDEN' | 'REMOVED',
    note?: string,
  ) {
    if (!['POST', 'STORY', 'COMMENT'].includes(targetType)) {
      throw new BadRequestException(`Invalid targetType: ${targetType}`);
    }
    if (!['VISIBLE', 'HIDDEN', 'REMOVED'].includes(status)) {
      throw new BadRequestException(`Invalid status: ${status}`);
    }

    const data = {
      moderationStatus: status as $Enums.ModerationStatus,
      moderationNote: note,
    };

    let result: any;
    if (targetType === 'POST') {
      result = await this.prisma.post.update({ where: { id: targetId }, data });
    } else if (targetType === 'STORY') {
      result = await this.prisma.story.update({
        where: { id: targetId },
        data,
      });
    } else if (targetType === 'COMMENT') {
      result = await this.prisma.comment.update({
        where: { id: targetId },
        data,
      });
    }

    const action =
      status === 'VISIBLE'
        ? ('CONTENT_RESTORED' as any as AdminAction)
        : status === 'HIDDEN'
          ? AdminAction.CONTENT_RESTRICTED
          : AdminAction.CONTENT_REMOVED;

    await this.logAction(
      adminId,
      action,
      targetType.toLowerCase(),
      targetId,
      note,
    );

    return result;
  }

  // ─── Promotions ─────────────────────────────────────────────────

  /**
   * Paginated list of promotions with target details.
   */
  async getPromotions(
    page = 1,
    limit = 10,
    status?: PromotionStatus,
    search?: string,
  ) {
    const skip = (page - 1) * limit;

    const where: Prisma.PromotionWhereInput = {};
    if (status) where.status = status as any;
    if (search) {
      where.user = {
        OR: [
          { email: { contains: search, mode: 'insensitive' } },
          { profile: { username: { contains: search, mode: 'insensitive' } } },
        ],
      };
    }

    const [promotions, total] = await Promise.all([
      this.prisma.promotion.findMany({
        where,
        skip,
        take: limit,
        orderBy: { createdAt: 'desc' },
        include: {
          user: {
            include: { profile: true },
          },
        },
      }),
      this.prisma.promotion.count({ where }),
    ]);

    // Resolve targets (Posts/Stories)
    const enrichedPromotions = await Promise.all(
      promotions.map(async (promo) => {
        let target: any = null;
        if (promo.targetType === 'POST') {
          target = await this.prisma.post.findUnique({
            where: { id: promo.targetId },
            include: { media: true },
          });
        } else if (promo.targetType === 'STORY') {
          target = await this.prisma.story.findUnique({
            where: { id: promo.targetId },
          });
        }
        return { ...promo, target };
      }),
    );

    return {
      data: enrichedPromotions,
      meta: {
        total,
        page,
        limit,
        totalPages: Math.ceil(total / limit),
      },
    };
  }

  /**
   * Update promotion status (Approve/Reject).
   */
  async updatePromotionStatus(
    adminId: string,
    promotionId: string,
    status: PromotionStatus,
    note?: string,
  ) {
    const promo = await this.prisma.promotion.findUnique({
      where: { id: promotionId },
    });

    if (!promo) {
      throw new NotFoundException('Promotion not found');
    }

    const updated = await this.prisma.promotion.update({
      where: { id: promotionId },
      data: { status: status as any },
    });

    await this.logAction(
      adminId,
      status === PromotionStatus.ACTIVE
        ? AdminAction.UPDATE_USER_STATUS
        : AdminAction.DELETE_POST,
      'promotion',
      promotionId,
      `Status changed to ${status}${note ? `: ${note}` : ''}`,
    );

    // 4. Notify the user
    await this.notificationsService.create({
      recipientId: promo.userId,
      senderId: adminId,
      type: NotificationType.MODERATION,
      content:
        status === PromotionStatus.ACTIVE
          ? `¡Tu promoción ha sido aprobada! Tu contenido ahora llegará a más personas.`
          : `Tu solicitud de promoción ha sido rechazada.${
              note ? ` Motivo: ${note}` : ''
            }`,
      postId: promo.targetType === 'POST' ? promo.targetId : undefined,
    });

    return updated;
  }
