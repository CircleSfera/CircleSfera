  // ─── Reports ──────────────────────────────────────────────────────

  /** Paginated reports with optional search and status filter. */
  async getReports(page = 1, limit = 10, search?: string, status?: string) {
    const skip = (page - 1) * limit;
    const where: Prisma.ReportWhereInput = {};

    if (
      status &&
      ['PENDING', 'RESOLVED', 'DISMISSED', 'REVIEWING'].includes(status)
    ) {
      where.status = status as ReportStatus;
    }

    if (search) {
      const isReason = Object.values(ReportReason).includes(
        search.toUpperCase() as ReportReason,
      );

      where.OR = [
        ...(isReason ? [{ reason: search.toUpperCase() as ReportReason }] : []),
        { details: { contains: search, mode: 'insensitive' } },
        {
          reporter: {
            profile: {
              username: { contains: search, mode: 'insensitive' },
            },
          },
        },
      ];
    }

    const [reports, total] = await Promise.all([
      this.prisma.report.findMany({
        where,
        skip,
        take: limit,
        orderBy: { createdAt: 'desc' },
        include: {
          reporter: {
            include: {
              profile: {
                select: { username: true, avatar: true },
              },
            },
          },
        },
      }),
      this.prisma.report.count({ where }),
    ]);

    // Enrich reports with target content details
    const enriched = await Promise.all(
      reports.map(async (report) => {
        let targetContent: {
          thumbnail?: string | null;
          text?: string | null;
          type?: string;
          author?: string;
        } | null = null;

        try {
          if (report.targetType === 'POST') {
            const post = await this.prisma.post.findUnique({
              where: { id: report.targetId },
              select: {
                caption: true,
                type: true,
                media: { take: 1, select: { url: true } },
                user: { select: { profile: { select: { username: true } } } },
              },
            });
            if (post) {
              targetContent = {
                thumbnail: post.media?.[0]?.url || null,
                text: post.caption,
                type: post.type,
                author: post.user?.profile?.username,
              };
            }
          } else if (report.targetType === 'STORY') {
            const story = await this.prisma.story.findUnique({
              where: { id: report.targetId },
              select: {
                url: true,
                mediaType: true,
                user: { select: { profile: { select: { username: true } } } },
              },
            });
            if (story) {
              targetContent = {
                thumbnail: story.url,
                text: null,
                type: 'STORY',
                author: story.user?.profile?.username,
              };
            }
          } else if (report.targetType === 'COMMENT') {
            const comment = await this.prisma.comment.findUnique({
              where: { id: report.targetId },
              select: {
                content: true,
                url: true,
                user: { select: { profile: { select: { username: true } } } },
              },
            });
            if (comment) {
              targetContent = {
                thumbnail: comment.url || null,
                text: comment.content,
                type: 'COMMENT',
                author: comment.user?.profile?.username,
              };
            }
          }
        } catch (err) {
          console.error(
            `Error fetching report target ${report.targetId}:`,
            err,
          );
        }

        return { ...report, targetContent };
      }),
    );

    return {
      data: enriched,
      meta: {
        total,
        page,
        limit,
        totalPages: Math.ceil(total / limit),
      },
    };
  }

